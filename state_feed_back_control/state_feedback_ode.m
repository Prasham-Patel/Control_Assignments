function xd = state_feedback_ode(t, z)

xd = zeros(4,1);
z = num2cell(z);
[theta1, theta2, theta_dot1, theta_dot2] = deal(z{:});

if (abs(theta1) > 2*pi) 
    theta1 = mod(theta1, (2*pi));
end

if (abs(theta2) > 2*pi) 
    theta2 = mod(theta2, (2*pi));
end

x = [theta1; theta2; theta_dot1; theta_dot2];

A = [0,0,1,0;0,0,0,1;12.5768662816165,-11.9610915675817,0,0;-16.9227295511712,46.1565233491237,0,0];
B = [0,0;0,0;1.72504888391144,-4.43454974869380;-4.43454974869380,14.8902125302373];
p = [-4 -6 -3+2i -3-2i];
k = [71.6780712913934,25.7031512287617,22.4343895605068,8.67869135591330;20.8624193374030,11.9932842891210,6.91842521360113,3.12055792331669];


xd = (A-(B*k))*x;

end